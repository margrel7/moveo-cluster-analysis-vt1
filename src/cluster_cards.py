def build_section(examples: list, side: str, n_total_side: int) -> str:
    header_class = "section-good" if side == "good" else "section-prob"
    quote_class  = "example-quote-good" if side == "good" else "example-quote-prob"
    label        = "Works Well" if side == "good" else "Problems"

    examples_html = ""

    if not examples:
        examples_html = '<div class="empty">No reports in this category.</div>'
    else:
        for row in examples:
            # Photo
            path = row.get("local_image_path", "")
            b64  = image_to_base64(str(path)) if pd.notna(path) else None

            if b64:
                photo_html = f'<img src="data:image/jpeg;base64,{b64}" alt="">'
                photo_div  = f'<div class="example-photo">{photo_html}</div>'
            else:
                photo_div  = '<div class="example-photo no-image">no photo</div>'

            # Text — use raw_text, fall back to topic
            text = str(row.get("raw_text", "") or "").strip()
            if not text or text == "nan":
                text = str(row.get("topic", "") or "").strip()
            if not text or text == "nan":
                text = "—"
            if len(text) > 240:
                text = text[:237] + "..."

            examples_html += f"""
            <div class="example">
                {photo_div}
                <div class="example-text">
                    <div class="example-quote {quote_class}">"{text}"</div>
                </div>
            </div>"""

    return f"""
    <div class="section">
        <div class="section-header {header_class}">
            <span>{label}</span>
            <span class="section-count">{n_total_side} reports</span>
        </div>
        {examples_html}
    </div>"""


def build_card_html(
    cluster_id: int,
    cluster_df: pd.DataFrame,
    embeddings: np.ndarray,
    centroid: np.ndarray,
) -> str:
    label   = CLUSTER_LABELS[cluster_id]
    n_total = len(cluster_df)
    n_good  = int((cluster_df["category"] == "works_well").sum())
    n_prob  = int((cluster_df["category"] == "problem").sum())

    # Adapt max examples based on dominance
    # If one side is small, show fewer examples from it
    def examples_for_side(n_side: int) -> int:
        if n_side == 0:   return 0
        if n_side <= 2:   return min(n_side, 1)
        if n_side <= 4:   return min(n_side, 2)
        return MAX_EXAMPLES_PER_SIDE

    # Get representative paired examples
    good_mask = cluster_df["category"] == "works_well"
    prob_mask = cluster_df["category"] == "problem"

    good_examples = get_diverse_pairs(
        cluster_df[good_mask].reset_index(drop=True),
        embeddings[good_mask.values],
        centroid,
        examples_for_side(n_good),
    )
    prob_examples = get_diverse_pairs(
        cluster_df[prob_mask].reset_index(drop=True),
        embeddings[prob_mask.values],
        centroid,
        examples_for_side(n_prob),
    )

    good_section = build_section(good_examples, "good", n_good)
    prob_section = build_section(prob_examples, "prob", n_prob)

    return f"""
    <div class="card">
        <div class="card-header">
            <div class="cluster-number">Cluster {cluster_id}</div>
            <div class="cluster-title-en">{label['en']}</div>
            <div class="cluster-title-de">{label['de']}</div>
            <div class="header-stats">
                <span><span class="stat-dot" style="background:#5BB450"></span>{n_good} works well</span>
                <span><span class="stat-dot" style="background:#D94F37"></span>{n_prob} problems</span>
                <span style="color:#aaa">{n_total} reports total</span>
            </div>
        </div>

        {good_section}
        <hr class="section-divider">
        {prob_section}
    </div>"""


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def generate_cluster_cards(
    results_csv: str,
    embeddings_npy: str,
    output_html: str,
):
    print("Loading data...")
    df         = pd.read_csv(results_csv)
    embeddings = np.load(embeddings_npy)

    # Merge image paths and text
    llava = pd.read_csv("outputs/preprocessed_llava.csv")[
        ["photo_id", "raw_text", "local_image_path"]
    ]
    topics = pd.read_csv("data/raw/raw_data.csv")[["photo_id", "topic"]]

    df = df.merge(llava,   on="photo_id", how="left")
    df = df.merge(topics,  on="photo_id", how="left")

    print(f"Loaded {len(df)} reports.\n")

    cards_html = ""
    for cluster_id in sorted(df["hdbscan_label_assigned"].unique()):
        mask         = df["hdbscan_label_assigned"] == cluster_id
        cluster_df   = df[mask].reset_index(drop=True)
        cluster_embs = embeddings[mask.values]
        centroid     = cluster_embs.mean(axis=0)

        n_good = (cluster_df["category"] == "works_well").sum()
        n_prob = (cluster_df["category"] == "problem").sum()
        print(f"  Cluster {cluster_id}: {len(cluster_df)} reports ({n_good} good, {n_prob} problems)")

        cards_html += build_card_html(cluster_id, cluster_df, cluster_embs, centroid)

    html = f"""<!DOCTYPE html>
<html lang="de">
<head>
    <meta charset="UTF-8">
    <title>MOVEO Workshop — Cluster Cards</title>
    <style>{CSS}</style>
</head>
<body>
{cards_html}
</body>
</html>"""

    with open(output_html, "w", encoding="utf-8") as f:
        f.write(html)

    print(f"\nDone — {output_html}")
    print("Open in browser → Print → A4 portrait, no margins.")


