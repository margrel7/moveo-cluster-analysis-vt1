def image_to_base64(filepath: str) -> str | None:
    try:
        with open(filepath, "rb") as f:
            return base64.b64encode(f.read()).decode("utf-8")
    except Exception:
        return None


def get_diverse_pairs(
    subset_df: pd.DataFrame,
    subset_embs: np.ndarray,
    centroid: np.ndarray,
    max_examples: int,
) -> list:
    """
    Select a diverse set of complete report pairs (photo + text from same report).

    Strategy:
        - Pick 1 report closest to the centroid (most typical)
        - Pick remaining reports to maximise spread across the cluster
          using a greedy furthest-point selection — each new pick is
          the report furthest from all already-selected reports
        - Only considers reports with both a valid image AND text
        - Falls back to image-only if needed to fill slots

    This ensures the workshop card shows the range of what is in the
    cluster, not just multiple near-identical central examples.
    """
    if len(subset_df) == 0:
        return []

    # Filter to usable reports (image + text preferred)
    usable = []
    usable_embs = []
    for i, (_, row) in enumerate(subset_df.iterrows()):
        has_image = (
            pd.notna(row.get("local_image_path"))
            and os.path.exists(str(row.get("local_image_path", "")))
        )
        text = str(row.get("raw_text", "") or "").strip()
        has_text = text and text != "nan" and len(text) > 10
        if has_image and (has_text or len(usable) < max_examples):
            usable.append(row)
            usable_embs.append(subset_embs[i])

    if not usable:
        return []

    embs = np.array(usable_embs)

    # Start with the report closest to the centroid
    dists_to_centroid = cosine_distances(embs, centroid.reshape(1, -1)).flatten()
    first = int(np.argmin(dists_to_centroid))
    selected_indices = [first]

    # Greedily add the report furthest from all selected reports
    while len(selected_indices) < min(max_examples, len(usable)):
        selected_embs = embs[selected_indices]
        # For each candidate, find its minimum distance to any selected report
        min_dists = []
        for i in range(len(usable)):
            if i in selected_indices:
                min_dists.append(-1)  # already selected
                continue
            dists = cosine_distances(embs[i].reshape(1, -1), selected_embs).flatten()
            min_dists.append(float(np.min(dists)))
        next_pick = int(np.argmax(min_dists))
        selected_indices.append(next_pick)

    return [usable[i] for i in selected_indices]


