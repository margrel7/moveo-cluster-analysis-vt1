def build_header(cluster_id: int, n_good: int, n_prob: int, n_total: int) -> str:
    label = CLUSTER_LABELS.get(cluster_id, {"en": f"Cluster {cluster_id}", "de": f"Cluster {cluster_id}"})
    return f"""
    <div class="card-header">
        <div class="cluster-number">Cluster {cluster_id}</div>
        <div class="cluster-title-en">{label['en']}</div>
        <div class="cluster-title-de">{label['de']}</div>
        <div class="header-stats">
            <span><span class="stat-dot" style="background:#5BB450"></span>{n_good} works well</span>
            <span><span class="stat-dot" style="background:#D94F37"></span>{n_prob} problems</span>
            <span style="color:#aaa">{n_total} reports total</span>
        </div>
    </div>"""


def build_section(examples: list, side: str, n_total_side: int) -> str:
    header_class = "section-good" if side == "good" else "section-prob"
    quote_class  = "example-quote-good" if side == "good" else "example-quote-prob"
    label        = "Works Well" if side == "good" else "Problems"

    if not examples:
        examples_html = '<div class="empty">No reports in this category.</div>'
    else:
        examples_html = ""
        for row in examples:
            path = row.get("local_image_path", "")
            b64  = image_to_base64(str(path)) if pd.notna(path) else None
            if b64:
                photo_div = f'<div class="example-photo"><img src="data:image/jpeg;base64,{b64}" alt=""></div>'
            else:
                photo_div = '<div class="example-photo no-image">no photo</div>'
            text = str(row.get("raw_text", "") or "").strip()
            if not text or text == "nan":
                text = str(row.get("topic", "") or "").strip()
            if not text or text == "nan":
                text = "—"
            if len(text) > 160:
                text = text[:157] + "..."
            examples_html += f"""
            <div class="example">
                {photo_div}
                <div class="example-text">
                    <div class="example-quote {quote_class}">"{text}"</div>
                </div>
            </div>"""

    return f"""
    <div class="section">
        <div class="section-header {header_class}">
            <span>{label}</span>
            <span class="section-count">{n_total_side} reports</span>
        </div>
        {examples_html}
    </div>"""


def build_card_page1(
    cluster_id: int,
    cluster_df: pd.DataFrame,
    embeddings: np.ndarray,
    centroid: np.ndarray,
) -> str:
    n_total = len(cluster_df)
    n_good  = int((cluster_df["category"] == "works_well").sum())
    n_prob  = int((cluster_df["category"] == "problem").sum())

    def examples_for_side(n: int) -> int:
        if n == 0: return 0
        if n <= 2: return min(n, 1)
        if n <= 4: return min(n, 2)
        return MAX_EXAMPLES_PER_SIDE

    good_mask = cluster_df["category"] == "works_well"
    prob_mask = cluster_df["category"] == "problem"

    good_examples = get_diverse_pairs(
        cluster_df[good_mask].reset_index(drop=True),
        embeddings[good_mask.values], centroid, examples_for_side(n_good),
    )
    prob_examples = get_diverse_pairs(
        cluster_df[prob_mask].reset_index(drop=True),
        embeddings[prob_mask.values], centroid, examples_for_side(n_prob),
    )

    return f"""
    <div class="card">
        {build_header(cluster_id, n_good, n_prob, n_total)}
        {build_section(good_examples, "good", n_good)}
        <hr class="section-divider">
        {build_section(prob_examples, "prob", n_prob)}
    </div>"""


def build_card_page2(
    cluster_id: int,
    cluster_df: pd.DataFrame,
    all_df: pd.DataFrame,
) -> str:
    n_total = len(cluster_df)
    n_good  = int((cluster_df["category"] == "works_well").sum())
    n_prob  = int((cluster_df["category"] == "problem").sum())

    map_b64 = generate_cluster_map_image(cluster_df, all_df, cluster_id)
    if map_b64:
        map_html = f"""
        <div class="cluster-map">
            <img src="data:image/png;base64,{map_b64}" alt="Map of cluster {cluster_id}">
            <div class="cluster-map-legend">
                <span><span class="map-legend-dot" style="background:#5BB450"></span>Works well</span>
                <span><span class="map-legend-dot" style="background:#D94F37"></span>Problem</span>
                <span><span class="map-legend-dot" style="background:#cccccc"></span>Other clusters</span>
            </div>
        </div>"""
    else:
        map_html = '<div class="empty">No location data available for this cluster.</div>'

    return f"""
    <div class="card-map">
        {build_header(cluster_id, n_good, n_prob, n_total)}
        <div class="map-page-body">
            {map_html}
        </div>
    </div>"""


