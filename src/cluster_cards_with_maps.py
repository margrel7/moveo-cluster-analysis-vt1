def generate_cluster_cards(
    results_csv: str,
    embeddings_npy: str,
    output_html: str,
):
    print("Loading data...")
    df         = pd.read_csv(results_csv)
    embeddings = np.load(embeddings_npy)

    llava  = pd.read_csv("outputs/preprocessed_llava.csv")[["photo_id", "raw_text", "local_image_path"]]
    topics = pd.read_csv("data/raw/raw_data.csv")[["photo_id", "topic"]]
    df = df.merge(llava,   on="photo_id", how="left")
    df = df.merge(topics,  on="photo_id", how="left")

    print(f"Loaded {len(df)} reports.\n")

    cards_html = ""
    for cluster_id in sorted(df["hdbscan_label_assigned"].unique()):
        mask         = df["hdbscan_label_assigned"] == cluster_id
        cluster_df   = df[mask].reset_index(drop=True)
        cluster_embs = embeddings[mask.values]
        centroid     = cluster_embs.mean(axis=0)

        n_good = (cluster_df["category"] == "works_well").sum()
        n_prob = (cluster_df["category"] == "problem").sum()
        print(f"  Cluster {cluster_id}: {len(cluster_df)} reports ({n_good} good, {n_prob} problems)")

        cards_html += build_card_page1(cluster_id, cluster_df, cluster_embs, centroid)
        cards_html += build_card_page2(cluster_id, cluster_df, df)

    html = f"""<!DOCTYPE html>
<html lang="de">
<head>
    <meta charset="UTF-8">
    <title>MOVEO Workshop — Cluster Cards</title>
    <style>{CSS}</style>
</head>
<body>
{cards_html}
</body>
</html>"""

    with open(output_html, "w", encoding="utf-8") as f:
        f.write(html)

    n_clusters = len(df["hdbscan_label_assigned"].unique())
    print(f"\nDone — {output_html}")
    print(f"Total pages: {2 * n_clusters} (2 per cluster)")
    print("Open in browser → Print → A4, no margins.")


