def image_to_base64(filepath: str) -> str | None:
    try:
        with open(filepath, "rb") as f:
            return base64.b64encode(f.read()).decode("utf-8")
    except Exception:
        return None


def generate_cluster_map_image(
    cluster_df: pd.DataFrame,
    all_df: pd.DataFrame,
    cluster_id: int,
) -> str | None:
    if not HAS_STATICMAP:
        return None
    geo = cluster_df[cluster_df["lat"].notna() & cluster_df["long"].notna()]
    if geo.empty:
        return None
    m = StaticMap(MAP_WIDTH, MAP_HEIGHT, url_template=TILE_URL)
    other = all_df[
        (all_df["hdbscan_label_assigned"] != cluster_id)
        & all_df["lat"].notna()
        & all_df["long"].notna()
    ]
    for _, row in other.iterrows():
        m.add_marker(CircleMarker((float(row["long"]), float(row["lat"])), "#cccccc", 8))
    for _, row in geo.iterrows():
        fill = "#5BB450" if row.get("category") == "works_well" else "#D94F37"
        m.add_marker(CircleMarker((float(row["long"]), float(row["lat"])), fill, 14))
    try:
        image = m.render(zoom=MAP_ZOOM, center=WINTERTHUR_CENTER)
        buffer = io.BytesIO()
        image.save(buffer, format="PNG")
        return base64.b64encode(buffer.getvalue()).decode("utf-8")
    except Exception as e:
        print(f"    Map render failed for cluster {cluster_id}: {e}")
        return None


def get_diverse_pairs(
    subset_df: pd.DataFrame,
    subset_embs: np.ndarray,
    centroid: np.ndarray,
    max_examples: int,
) -> list:
    if len(subset_df) == 0:
        return []
    distances = cosine_distances(subset_embs, centroid.reshape(1, -1)).flatten()
    order     = np.argsort(distances)
    sorted_df = subset_df.iloc[order].reset_index(drop=True)
    complete = []
    for _, row in sorted_df.iterrows():
        if len(complete) >= max_examples:
            break
        has_image = (
            pd.notna(row.get("local_image_path"))
            and os.path.exists(str(row.get("local_image_path", "")))
        )
        text     = str(row.get("raw_text", "") or "").strip()
        has_text = text and text != "nan" and len(text) > 10
        if has_image and has_text:
            complete.append(row)
    if len(complete) < max_examples:
        for _, row in sorted_df.iterrows():
            if len(complete) >= max_examples:
                break
            if row["photo_id"] in [r["photo_id"] for r in complete]:
                continue
            has_image = (
                pd.notna(row.get("local_image_path"))
                and os.path.exists(str(row.get("local_image_path", "")))
            )
            if has_image:
                complete.append(row)
    return complete


